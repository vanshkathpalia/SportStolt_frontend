export const AddStory = () => {
    const [location, setLocation] = useState("");
    const [image, setImage] = useState("");

    const navigate = useNavigate();

    return <div className="flex flex-row">
        <div className = "pt-6 px-4 min-w-96" >
            <Appbar />
        </div>

        <div className="flex justify-center w-full pt-8"> 
            <div className="max-w-screen-lg w-full">
                <input onChange={(e) => {
                    setLocation(e.target.value)
                }} type="text" className="w-full bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block p-2.5" placeholder="Enter the location" />

                <TextEditor onChange={(e) => {
                    setImage(e.target.value)
                }} />
                <button onClick={async () => {
                        await axios.post(`${BACKEND_URL}/api/v1/story`, {
                            location,
                            image: image,
                            isViewed: false
                        }, {
                            headers: {
                                Authorization: localStorage.getItem("token")
                            }
                        });
                    navigate(`/post`)
                }} type="submit" className="mt-4 inline-flex items-center px-5 py-2.5 text-sm font-medium text-center text-white bg-blue-700 rounded-lg focus:ring-4 focus:ring-blue-200 dark:focus:ring-blue-900 hover:bg-blue-800">
                    Publish Story 
                </button>
            </div>
        </div>
    </div>
}


function TextEditor({ onChange }: {onChange: (e: ChangeEvent<HTMLTextAreaElement>) => void}) {
    return <div className="mt-2">
        <div className="w-full mb-4 ">
            <div className="flex items-center justify-between border">
            <div className="my-2 bg-white rounded-b-lg w-full">
                <label className="sr-only">Publish Story</label>
                <textarea onChange={onChange} id="editor" rows={8} className="focus:outline-none block w-full px-0 text-sm text-gray-800 bg-white border-0 pl-2" placeholder="Write the image link..." required />
            </div>
        </div>
       </div>
    </div>
    
}

export default AddStory;
